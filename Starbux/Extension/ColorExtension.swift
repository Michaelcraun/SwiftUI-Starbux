import SwiftUI

extension Color {
    static var creamWhite: Color {
        return Color("Cream White")
    }
    
    static var creamDark: Color {
        return Color("Cream Dark")
    }
    
    static var darkGreen: Color {
        return Color("Dark Green")
    }
    
    static var lightGreen: Color {
        return Color("Light Green")
    }
}
